print("Hola, bienvenido a la calculadora")
numero1 =  int(input("inserte primer numero: "))
numero2 =  int(input("inserte segundo numero: "))
operacion = input("inserta simbolo de operacion(solo puedes insertar +,-,*,/)")

if operacion == "+":
    resultado = numero1 + numero2
    print("el resultado de su suma es:", resultado)

elif operacion == "-":
    resultado = numero1 - numero2
    print("el resultado de su resta es ", resultado)

elif operacion == "*":
    resultado = numero1 * numero2
    print("el restultado de tu multiplacion es ", resultado)










     










